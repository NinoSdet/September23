package December19.inheritanceDemo.shapes;

public class Rectangle extends Shape {



    double width;
    double height;


    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
