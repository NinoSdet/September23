package september28;

public class EscapeChars {


    public static void main(String[] args) {

        System.out.println("He said \"\"\"Java is fun\"\"\"");

        String path = "C:\\Users\\Nuclues";

        System.out.println(path);

        System.out.println("///\\\\\\");    ///\\\

        System.out.println("Java\tis\tawesome\tand\tchallenging");

        System.out.println("Java\nis\nawesome");

        System.out.println("Name: John\nLast Name: Doe");

    }
}